package edu.studentOrder.domaine;

public class StudentOrder {
    private String husbandFirstName;
    private String husbandSecondName;
    private String wifeFirstName;
    private String wifeSecondName;

    public String getHusbandFirstName() {
        return husbandFirstName;
    }

    public void setHusbandFirstName(String husbandFirstName) {
        this.husbandFirstName = husbandFirstName;
    }

    public String getHusbandSecondName() {
        return husbandSecondName;
    }

    public void setHusbandSecondName(String husbandSecondName) {
        this.husbandSecondName = husbandSecondName;
    }

    public String getWifeFirstName() {
        return wifeFirstName;
    }

    public void setWifeFirstName(String wifeFirstName) {
        this.wifeFirstName = wifeFirstName;
    }

    public String getWifeSecondName() {
        return wifeSecondName;
    }

    public void setWifeSecondName(String wifeSecondName) {
        this.wifeSecondName = wifeSecondName;
    }
}
